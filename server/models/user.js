const mongoose = require("mongoose");

const userSchema = mongoose.Schema({
  userName: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
  role: {
    type: String,
    enum: ["admin", "viewer"],
    default: "viewer", // Set default role if not specified
  },
});

const User = mongoose.model("User", userSchema);
module.exports = User;
